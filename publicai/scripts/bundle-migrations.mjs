#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const migrationsDir = path.join(root, "supabase", "migrations");
const outFile = path.join(root, "api", "lib", "migrationSql.js");

const files = fs.readdirSync(migrationsDir).filter((f) => f.endsWith(".sql")).sort();
const migrations = files.map((name) => ({
  name,
  sql: fs.readFileSync(path.join(migrationsDir, name), "utf8"),
}));

const contents = `// Generated by scripts/bundle-migrations.mjs — do not edit manually
const MIGRATIONS = ${JSON.stringify(migrations, null, 2)};

module.exports = { MIGRATIONS };
`;

fs.writeFileSync(outFile, contents, "utf8");
console.log(`Bundled ${migrations.length} migrations → api/lib/migrationSql.js`);
